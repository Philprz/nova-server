class ProgressTracker:
    """Gestionnaire global des t√¢ches de progression"""
    
    def __init__(self):
        self.active_tasks: Dict[str, QuoteTask] = {}
        self.completed_tasks: List[Dict[str, Any]] = []
        self.max_completed_history = 50  # Garder les 50 derni√®res t√¢ches
    
    def create_task(self, task_id: Optional[str] = None, *, title: Optional[str] = None, user_prompt: str = "", draft_mode: bool = False) -> QuoteTask:
        """Cr√©e (ou r√©cup√®re) une t√¢che de g√©n√©ration de devis avec idempotence et journalisation."""
        if task_id is None:
            task_id = self._generate_task_id(prefix="quote")

        # V√©rification d'idempotence
        if task_id in self.active_tasks:
            logger.info(f"‚ôªÔ∏è T√¢che existante r√©cup√©r√©e: {task_id}")
            return self.active_tasks[task_id]
        task = QuoteTask(task_id=task_id, user_prompt=user_prompt, draft_mode=draft_mode)
        self.active_tasks[task.task_id] = task
        logger.info(f"üÜï Nouvelle t√¢che cr√©√©e: {task.task_id}")
        return task

    
    def get_task(self, task_id: str) -> Optional[QuoteTask]:
        """R√©cup√®re une t√¢che par son ID"""
        return self.active_tasks.get(task_id)
    
    def complete_task(self, task_id: str, result: Dict[str, Any]):
        """
        üîß MODIFICATION : Termine une t√¢che et sauvegarde le r√©sultat
        """
        if task_id not in self.active_tasks:
            logger.warning(f"‚ö†Ô∏è Tentative de completion t√¢che inexistante: {task_id}")
            return False

        task = self.active_tasks[task_id]

        # S√©curiser la compl√©tion de la t√¢che
        try:
            task.complete_task(result)
        except Exception as e:
            logger.error(f"‚ùå √âchec complete_task({task_id}): {e}")
            # On continue quand m√™me l‚Äôarchivage minimal
            # (option: return False si tu veux un √©chec strict)
        
        # üîß MODIFICATION : Sauvegarder le r√©sultat dans l'historique
        try:
            task_data = task.get_overall_progress()
        except Exception as e:
            logger.error(f"‚ùå get_overall_progress({task_id}) a √©chou√©: {e}")
            task_data = {"task_id": task_id, "progress": None}
        # s'assurer d'une copie ind√©pendante
        task_data = dict(task_data) if isinstance(task_data, dict) else {"task_id": task_id}
        task_data["result"] = result  # Ajouter le r√©sultat
        self.completed_tasks.append(task_data)

        # Limiter la taille de l'historique
        if len(self.completed_tasks) > self.max_completed_history:
            self.completed_tasks = self.completed_tasks[-self.max_completed_history:]

        # Supprimer des t√¢ches actives
        del self.active_tasks[task_id]
        
        # üîß CORRECTION CRITIQUE: Notification WebSocket de completion avec d√©lai de fermeture
        try:
            from services.websocket_manager import websocket_manager
            from datetime import datetime
            import asyncio
            import threading

            payload = {
                "type": "quote_generation_completed",
                "task_id": task_id,
                "data": result,
                "status": "completed",
                "timestamp": datetime.now().isoformat()
                }

            async def _notify_and_close():
                try:
                    await websocket_manager.broadcast_to_task(task_id, payload)
                    logger.info(f"üîî Notification WebSocket de completion envoy√©e pour {task_id}")
                except Exception as e:
                    logger.error(f"Erreur broadcast WebSocket completion: {e}")
                    return
                try:
                    await asyncio.sleep(2.0)  # Laisser le client traiter
                    await websocket_manager.close_task_connections(task_id)
                    logger.info(f"üîå Fermeture des connexions WebSocket pour {task_id}")
                except Exception as e:
                    logger.error(f"Erreur fermeture WebSocket pour {task_id}: {e}")

            # Fire-and-forget robuste (boucle existante ou non)
            def _spawn(coro):
                try:
                    loop = asyncio.get_running_loop()
                    loop.create_task(coro)
                except RuntimeError:
                    # Pas de boucle en cours: lancer un thread d√©di√© avec asyncio.run
                    threading.Thread(target=lambda: asyncio.run(coro), daemon=True).start()

            _spawn(_notify_and_close())

        except Exception as e:
            logger.error(f"Erreur notification WebSocket completion: {e}")
        
        logger.info(f"‚úÖ T√¢che {task_id} d√©plac√©e vers l'historique avec r√©sultat")
        return True

    
    def fail_task(self, task_id: str, error: str):
        """Termine une t√¢che en erreur"""
        if task_id not in self.active_tasks:
            return False
            
        task = self.active_tasks[task_id]
        task.fail_task(error)
        
        # D√©placer vers l'historique
        self.completed_tasks.append(task.get_overall_progress())
        
        # Supprimer des t√¢ches actives
        del self.active_tasks[task_id]
        # üîß CORRECTION: Notification WebSocket d'√©chec
        try:
            from services.websocket_manager import websocket_manager
            import asyncio
            
            asyncio.create_task(websocket_manager.broadcast_to_task(task_id, {
                "type": "error",
                "task_id": task_id,
                "error": error,
                "status": "failed",
                "timestamp": datetime.now().isoformat()
            }))
            logger.info(f"üîî Notification WebSocket d'erreur envoy√©e pour {task_id}")
        except Exception as e:
            logger.error(f"Erreur notification WebSocket √©chec: {e}")
        return True
    
    def get_all_active_tasks(self) -> List[Dict[str, Any]]:
        """Retourne toutes les t√¢ches actives"""
        return [task.get_overall_progress() for task in self.active_tasks.values()]
    
    def get_task_history(self) -> List[Dict[str, Any]]:
        """Retourne l'historique des t√¢ches termin√©es"""
        return self.completed_tasks.copy()

